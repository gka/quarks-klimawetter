    <h3>Infobox: Was ist eigentlich normal?</h3>

    <p>Das Wetter schwankt ständig. Das Klima dagegen verändert sich sehr langsam und ist – wenn man so will – nichts anderes als das Wetter betrachtet über einen langen Zeitraum. Angaben über das Wetter sind örtlich und zeitlich begrenzt. Das Klima dagegen ist ein Mittelwert des Wetters, den Meteorologen über einen Zeitraum von mindestens 30 Jahren bilden. Auch Extremwerte aus dieser Periode fließen in die Statistik mit ein. Um Wetter und Klima zu vergleichen, nutzt man deshalb Referenzperioden, die das Wetter (Temperatur, Niederschlag usw.) über <strong>mindestens 30 Jahre</strong> in Durchschnittswerten zusammenfassen.</p>

    <p>Wir nutzen hier die Referenzperiode der Jahre <strong>1961-1990</strong>: Sie waren noch kaum von der Erderwärmung betroffen und gelten daher als offizielle Vergleichsperiode, um Veränderungen durch den Klimawandel aufzuzeigen. Der <strong>graue Bereich</strong> in unseren Diagrammen zeigt also, welches Wetter von 1961-1990 ((im Schnitt)) am häufigsten war. Wir bezeichnen es deshalb als “normal”. Liegt die aktuelle Temperatur oder der aktuelle Niederschlag außerhalb des grauen Bereichs, ist es somit überdurchschnittlich warm/kalt bzw. regnerisch/trocken. (Mehr Details zu unserer Referenzperiode findest du weiter unten).</p>

