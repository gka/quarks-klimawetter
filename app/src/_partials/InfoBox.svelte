        <h2>Was ist eigentlich normal?</h2>
    </div>
    <div class="color_box_content">
        <p>
            Mit “normal” bezeichnen wir die mittleren Wetterbedingungen der Jahre <strong
                >1961-1990</strong
            >. Sie waren noch vergleichsweise wenig von der menschengemachten Erderwärmung betroffen
            und gelten daher als offizielle und internationale Vergleichsperiode, um Veränderungen
            durch den Klimawandel aufzuzeigen.
        </p>

        <p>
            Das Wetter schwankt ständig und ist zeitlich begrenzt. Das Klima dagegen verändert sich
            sehr langsam und ist – wenn man so will – nichts anderes als das Wetter betrachtet über
            einen langen Zeitraum. Das Klima ist also kurz gesagt der Durchschnitt des Wetters, den
            Meteorologen über einen Zeitraum von mindestens 30 Jahren bilden. Auch Extremwerte aus
            dieser Periode fließen in die Statistik mit ein. Um Wetter und Klima zu vergleichen,
            nutzt man Referenzperioden, die das Wetter (Temperatur, Niederschlag usw.) über <strong
                >mindestens 30 Jahre</strong
            > in Durchschnittswerten zusammenfassen.
        </p>

        <p>
            Der <strong>graue Bereich</strong> in unseren Diagrammen zeigt also, welches Wetter von
            1961-1990 regelmäßig gemessen wurde. Wie wir den grauen Bereich genau eingegrenzt haben,
            erfährst du unter <a href="#datengrundlage-wetterklima">Unsere Datengrundlage</a>. Liegt
            die aktuelle Temperatur oder der aktuelle Niederschlag außerhalb des grauen Bereichs,
            ist es somit überdurchschnittlich warm/kalt bzw. regnerisch/trocken.
        </p>
