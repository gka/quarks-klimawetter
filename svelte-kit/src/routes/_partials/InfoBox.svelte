    <h3>Infobox: Was ist eigentlich normal?</h3>

    <p>Mit “normal” meinen wir die Wetterschwankungen der Jahre <strong>1961-1990</strong>. Sie waren noch vergleichsweise wenig von der Erderwärmung betroffen und gelten daher als offizielle und internationale Vergleichsperiode, um Veränderungen durch den Klimawandel aufzuzeigen. </p>

    <p>Das Wetter schwankt ständig. Das Klima dagegen verändert sich sehr langsam und ist – wenn man so will – nichts anderes als das Wetter betrachtet über einen langen Zeitraum. Angaben über das Wetter sind örtlich und zeitlich begrenzt. Das Klima ist also ein Mittelwert des Wetters, den Meteorologen über einen Zeitraum von mindestens 30 Jahren bilden. Auch Extremwerte aus dieser Periode fließen in die Statistik mit ein. Um Wetter und Klima zu vergleichen, nutzt man Referenzperioden, die das Wetter (Temperatur, Niederschlag usw.) über <strong>mindestens 30 Jahre</strong> in Durchschnittswerten zusammenfassen.</p>

    <p>Der <strong>graue Bereich</strong> in unseren Diagrammen zeigt also, welches Wetter von 1961-1990 am häufigsten war. Wie wir den grauen Bereich genau eingegrenzt haben, erfährst du unter <a href="#datengrundlage">Unsere Datengrundlage</a>. Liegt die aktuelle Temperatur oder der aktuelle Niederschlag außerhalb des grauen Bereichs, ist es somit überdurchschnittlich warm/kalt bzw. regnerisch/trocken.</p>

